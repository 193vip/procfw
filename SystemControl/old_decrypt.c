void patch_mesgled_for_updater(u32 mesgled_textaddr)
{
	u32 intr;

	sub_000000e0 = (void*)(mesgled_textaddr + 0xe0);
	intr = MAKE_CALL(myMesgLed_sub_000000e0_wrapper);

	switch(psp_model) {
		case PSP_1000:
			_sw(intr, mesgled_textaddr+0x1B68); 
			// sceMesgLed_driver_33FC774F: 0x0200 decryptor
			_sw(intr, mesgled_textaddr+0x3884);
			// sceMesgLed_driver_81F72B1F: 0x0B00(updater) decryptor
			break;
		case PSP_2000:
			_sw(intr, mesgled_textaddr+0x1BC0); 
			_sw(intr, mesgled_textaddr+0x3DF4);
			break;
		case PSP_9000:
		case PSP_7000:
		case PSP_4000:
		case PSP_3000:
			_sw(intr, mesgled_textaddr+0x1C08); 
			_sw(intr, mesgled_textaddr+0x431C);
			break;
		case PSP_GO:
			_sw(intr, mesgled_textaddr+0x1C54); 
			_sw(intr, mesgled_textaddr+0x47F0);
			break;
		default:
			printk("%s: unknown model %dg\n", __func__, psp_model+1);
			break;
	}
}
